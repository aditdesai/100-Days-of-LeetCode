# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def minDepth(self, root: Optional[TreeNode]) -> int:
        ''' 
        Idea 1 - level order traversal and the 1st leaf node encountered. 
        For this, use queue<pair<TreeNode *, int>> q; to keep track of level number (SEE CPP SOLUTION)
        '''
        # Idea 2 - just recursive
        if not root:
            return 0
        if not root.left:
            return 1 + self.minDepth(root.right)
        if not root.right:
            return 1 + self.minDepth(root.left)

        return 1 + min(self.minDepth(root.left), self.minDepth(root.right))